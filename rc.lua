local function client_menu_toggle_fn(c)
  local instance = nil

  return function (c)
    if instance and instance.wibox.visible then
      instance:hide()
      instance = nil
    else
      --instance = awful.menu.clients({ theme = { width = 250 } })
      instance = awful.menu({
        {
          'Float',
          function() c.floating = not c.floating end,
          c.floating and beautiful.titlebar_floating_button_focus_active or beautiful.titlebar_floating_button_focus_inactive
        },
        {
          'Maximize',
          function() 
            c.maximized_horizontal = not c.maximized_horizontal
            c.maximized_vertical = not c.maximized_horizontal
          end,
          c.maximized_horizontal and beautiful.titlebar_maximized_button_focus_active or beautiful.titlebar_maximized_button_focus_inactive
        },
        {
          'Stick',
          function(state) c.sticky = not c.sticky end,
          c.sticky and beautiful.titlebar_sticky_button_focus_active or beautiful.titlebar_sticky_button_focus_inactive
        },
        {
          'To Top',
          function(state) c.ontop = not c.ontop end,
          c.ontop and beautiful.titlebar_ontop_button_focus_active or beautiful.titlebar_ontop_button_focus_inactive
        },
        {
          'Close',
          function () c:kill() end,
          beautiful.titlebar_close_button_focus
        }
      }):show()
    end
  end
end

